class Solution{
    Node divide(int N, Node head){
        if(head == null || head.next == null) return head;
        List<Integer> list = new ArrayList<>();
        Node temp = head;
        while(temp != null){
            list.add(temp.data);
            temp = temp.next;
        }
        List<Integer> odd = new ArrayList<>();
        List<Integer> even = new ArrayList<>();
        
        for(int i=0; i<list.size(); i++){
            if(list.get(i) % 2 == 0){
                even.add(list.get(i));
            }else{
                odd.add(list.get(i));
            }
        }
        even.addAll(odd);
        Node dummy = new Node(even.get(0));
        Node curr = dummy;
        for(int i=1; i<even.size(); i++){
            curr.next = new Node(even.get(i));
            curr = curr.next;
        }
        return dummy;
    }
}






